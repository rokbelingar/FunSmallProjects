x = input("What is your mood today? ")

if x == "happy":
    print("It is great to see you happy")
elif x == "nervous":
    print("Take a deep breathe 3 times")
elif x == "sad":
    print("watch something funny on youtube")
elif x == "excited":
    print("Woohoo, keep it up")
elif x == "relaxed":
    print("Read a book")
else:
    print("I don't recognize this mood")
